module.exports = {
    add: function (num1, num2) {
        return num1 + num2;
    },
    multiply: function (num1, num2) {
        return num1 * num2;
    },
    divide: function (num1, num2) {
        return num1 / num2;
    },
    difference: function (num1, num2) {
        return num1 - num2;
    }
}